using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FirstApp
{
    internal class ForLoopExample
    {
        public static void Main21()
        {
            int a = 10;
            for (int i = 1; i <= a; i++)
            {
                Console.WriteLine(i);
            }
            Console.ReadLine();
        }
        public static void Main4324()
        {
            int a = 10;
            int i = 1;
            while ( i <= a)
            {
                Console.WriteLine(i);
                i++;
            }
            Console.ReadLine();
        }
    }
}
